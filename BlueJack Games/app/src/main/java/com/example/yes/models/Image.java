package com.example.yes.models;

public class Image {
    private int img;
    private String imgtext;

    public Image(int img, String imgtext) {
        this.img = img;
        this.imgtext = imgtext;
    }

    public int getImg() {
        return img;
    }

    public String getImgtext() {
        return imgtext;
    }
}
